// TestParsePubspecLock_HostedDepsOnly verifies full parsing of a typical pubspec.lock
// with only hosted dependencies. Dep-type is mapped correctly per the
// "direct main" / "direct dev" / "transitive" field.
func TestParsePubspecLock_HostedDepsOnly(t *testing.T) {
	dir := t.TempDir()
	lockContent := `# Generated by pub
# See https://dart.dev/tools/pub/glossary#lockfile
packages:
  async:
    dependency: transitive
    description:
      name: async
      sha256: "947bfcf187f74dbc5e146c9eb9c0f10c9f8b30b"
      url: "https://pub.dartlang.org"
    source: hosted
    version: "2.11.0"
  http:
    dependency: "direct main"
    description:
      name: http
      sha256: "759701ea3fd9d27a0e5e6f9a6aa88e879a0e"
      url: "https://pub.dartlang.org"
    source: hosted
    version: "1.1.0"
  test:
    dependency: "direct dev"
    description:
      name: test
      sha256: "3d6e7963f3d7c44e3e5c7c3b2"
      url: "https://pub.dartlang.org"
    source: hosted
    version: "1.24.0"
sdks:
  dart: ">=3.0.0 <4.0.0"
`
	require.NoError(t, os.WriteFile(filepath.Join(dir, "pubspec.lock"), []byte(lockContent), 0o644))

	deps, complete, err := parsePubspecLock(dir)
	require.NoError(t, err)
	assert.True(t, complete, "valid pubspec.lock → complete=true")
	require.Len(t, deps, 3, "three hosted packages expected")

	byName := make(map[string]ResolvedDep)
	for _, d := range deps {
		byName[d.Name] = d
	}

	// "transitive" dependency → runtime
	asyncDep, ok := byName["async"]
	require.True(t, ok, "async package must be in closure")
	assert.Equal(t, "2.11.0", asyncDep.Version)
	assert.Equal(t, "runtime", asyncDep.DepType, "transitive dep must map to runtime")

	// "direct main" dependency → runtime
	httpDep, ok := byName["http"]
	require.True(t, ok, "http package must be in closure")
	assert.Equal(t, "1.1.0", httpDep.Version)
	assert.Equal(t, "runtime", httpDep.DepType, "direct main dep must map to runtime")

	// "direct dev" dependency → dev
	testDep, ok := byName["test"]
	require.True(t, ok, "test package must be in closure")
	assert.Equal(t, "1.24.0", testDep.Version)
	assert.Equal(t, "dev", testDep.DepType, "direct dev dep must map to dev")
}

// TestParsePubspecLock_NonHostedSkipped verifies that git, path, and sdk packages
// are excluded from the closure. These have no pub.dev registry identity and
// cannot match OSV Pub advisories; excluding them is safe (not false-clean).
func TestParsePubspecLock_NonHostedSkipped(t *testing.T) {
	dir := t.TempDir()
	lockContent := `packages:
  hosted_pkg:
    dependency: "direct main"
    description:
      name: hosted_pkg
      sha256: "abc123"
      url: "https://pub.dartlang.org"
    source: hosted
    version: "1.0.0"
  git_pkg:
    dependency: "direct main"
    description:
      ref: main
      resolved-ref: "deadbeef"
      url: "https://github.com/owner/repo.git"
    source: git
    version: "0.0.0"
  path_pkg:
    dependency: "direct main"
    description:
      path: "../local_pkg"
      relative: true
    source: path
    version: "0.1.0"
  sdk_flutter:
    dependency: transitive
    description: flutter
    source: sdk
    version: "0.0.0"
sdks:
  dart: ">=3.0.0 <4.0.0"
`
	require.NoError(t, os.WriteFile(filepath.Join(dir, "pubspec.lock"), []byte(lockContent), 0o644))

	deps, complete, err := parsePubspecLock(dir)
	require.NoError(t, err)
	assert.True(t, complete)
	require.Len(t, deps, 1, "only hosted packages must appear in the closure")
	assert.Equal(t, "hosted_pkg", deps[0].Name)
	assert.Equal(t, "1.0.0", deps[0].Version)
}

