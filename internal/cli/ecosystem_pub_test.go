// TestParsePubspecLock_FlutterProject verifies parsing of a realistic Flutter
// project lockfile that mixes sdk, hosted, and transitive deps.
func TestParsePubspecLock_FlutterProject(t *testing.T) {
	dir := t.TempDir()
	// Typical Flutter pubspec.lock: flutter itself is an sdk dep (skipped),
	// plus several hosted transitive deps.
	lockContent := `# Generated by pub
# See https://dart.dev/tools/pub/glossary#lockfile
packages:
  characters:
    dependency: transitive
    description:
      name: characters
      sha256: "04a925763edad35851ffe371d1741eba"
      url: "https://pub.dartlang.org"
    source: hosted
    version: "1.3.0"
  collection:
    dependency: transitive
    description:
      name: collection
      sha256: "ee67778f30230e04a1a5d1a9"
      url: "https://pub.dartlang.org"
    source: hosted
    version: "1.18.0"
  flutter:
    dependency: "direct main"
    description: flutter
    source: sdk
    version: "0.0.0"
  flutter_lints:
    dependency: "direct dev"
    description:
      name: flutter_lints
      sha256: "9e8c3a40"
      url: "https://pub.dartlang.org"
    source: hosted
    version: "3.0.0"
sdks:
  dart: ">=3.0.0 <4.0.0"
  flutter: ">=3.10.0"
`
	require.NoError(t, os.WriteFile(filepath.Join(dir, "pubspec.lock"), []byte(lockContent), 0o644))

	deps, complete, err := parsePubspecLock(dir)
	require.NoError(t, err)
	assert.True(t, complete)
	// sdk dep (flutter) must be excluded; 3 hosted deps remain
	require.Len(t, deps, 3, "flutter sdk dep excluded; 3 hosted deps remain")

	byName := make(map[string]ResolvedDep)
	for _, d := range deps {
		byName[d.Name] = d
	}

	assert.Equal(t, "1.3.0", byName["characters"].Version)
	assert.Equal(t, "runtime", byName["characters"].DepType)

	assert.Equal(t, "1.18.0", byName["collection"].Version)
	assert.Equal(t, "runtime", byName["collection"].DepType)

	assert.Equal(t, "3.0.0", byName["flutter_lints"].Version)
	assert.Equal(t, "dev", byName["flutter_lints"].DepType, "flutter_lints is a direct dev dep")

	_, flutterPresent := byName["flutter"]
	assert.False(t, flutterPresent, "flutter sdk dep must not appear in closure")
}

// ── pubDepType unit tests ─────────────────────────────────────────────────────

// TestPubDepType covers all known dependency field values from pubspec.lock
// and verifies the tri-state contract: only "direct dev" maps to "dev";
// everything else is runtime-or-unknown (never silently dev).
func TestPubDepType(t *testing.T) {
	t.Parallel()

	cases := []struct {
		input string
		want  string
	}{
		{"direct main", "runtime"},
		{"direct dev", "dev"},
		{"transitive", "runtime"},
		// Unrecognised values must default to "" (conservative; treated as runtime
		// by mergeDepType — unknown ≠ dev).
		{"", ""},
		{"unknown", ""},
		{"Direct Main", ""},  // case-sensitive match only
		{"direct test", ""},  // not a known pub dep type
	}

	for _, tc := range cases {
		tc := tc
		t.Run(tc.input, func(t *testing.T) {
			t.Parallel()
			got := pubDepType(tc.input)
			assert.Equal(t, tc.want, got, "pubDepType(%q)", tc.input)
		})
	}
}

