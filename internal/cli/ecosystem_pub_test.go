// TestPubAdapterRegistered verifies that the Pub Lane-A adapter is present in
// the global registry after package init().
func TestPubAdapterRegistered(t *testing.T) {
	var pubAdapter *LaneAAdapter
	for _, a := range LaneAAdapters() {
		a := a
		if a.Language == "dart" {
			pubAdapter = &a
			break
		}
	}
	require.NotNil(t, pubAdapter, "dart adapter must be registered after package init()")
	assert.Equal(t, "Pub", pubAdapter.Ecosystem, "dart adapter must use Pub ecosystem")
	assert.Contains(t, pubAdapter.DetectFiles, "pubspec.lock",
		"pubspec.lock must be a detect file for zero-config auto-detection")
	assert.Contains(t, pubAdapter.DetectFiles, "pubspec.yaml",
		"pubspec.yaml must be a detect file so manifest-only projects trigger incomplete")
	assert.NotNil(t, pubAdapter.ParseLockfile, "ParseLockfile must be set")
	assert.Nil(t, pubAdapter.NormalizeName,
		"NormalizeName must be nil (Pub names are case-sensitive and match OSV as-is)")
}

